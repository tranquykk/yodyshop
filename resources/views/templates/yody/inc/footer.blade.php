                } else if(sort == 'alpha-asc') {
                    $('.text-order').html('Từ A-Z');
                    $('.btn-quick-sort').removeClass('active');
                    $('.alpha-asc').addClass('active');
                } else if(sort == 'alpha-desc') {
                    $('.text-order').html('Từ Z-A');
                    $('.btn-quick-sort').removeClass('active');
                    $('.alpha-desc').addClass('active');
                } else if(sort == 'price-asc') {
                    $('.text-order').html('Giá tăng dần');
                    $('.btn-quick-sort').removeClass('active');
                    $('.price-asc').addClass('active');
                } else if(sort == 'price-desc') {
                    $('.text-order').html('Giá giảm dần');
                    $('.btn-quick-sort').removeClass('active');
                    $('.price-desc').addClass('active');
                } else if(sort == 'created-desc') {
                    $('.text-order').html('Mới nhất');
                    $('.btn-quick-sort').removeClass('active');
                    $('.created-desc').addClass('active');
                }

                $.ajax({
                    url: 'product-order/'+id,
                    type: 'GET',
                    data: {sort:sort, color:color},
                }).done(function(so){
                    console.log(so);
                    $("#order-div").empty();
                    $("#order-div").html(so);
                });
            }

            $(".filter-color").click(function() {
